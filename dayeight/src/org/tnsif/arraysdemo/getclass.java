package org.tnsif.arraysdemo;

public class getclass {

	public static void main(String[] args) {
		int[] a= {10,30,39};
		System.out.println(a.getClass().getName());
		
		float[] a1= {10.6f,30.8f,39.0f};
		System.out.println(a.getClass().getName());
		
		char[] a2= {'d','a','r','s','h'};
		System.out.println(a2.getClass().getName());

	}

}
